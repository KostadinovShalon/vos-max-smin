        self.null_space_red_dim = null_space_red_dim
        if null_space_red_dim > 0:
            self.fc = nn.Sequential(
                nn.Linear(512 * Bottleneck.expansion, null_space_red_dim),
                nn.ReLU(inplace=True),
                nn.Linear(null_space_red_dim, num_classes))
